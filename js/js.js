var slideIndex = 0;
var slideBigIndex = 0;

showSlides();
showBigSlides();

function showSlides() {
  var i;
  var slides = document.getElementsByClassName("images fade");
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none";
  }
  slideIndex++;
  if (slideIndex > slides.length) {slideIndex = 1}
  slides[slideIndex-1].style.display = "block";
  setTimeout(showSlides, 2000); // Change image every 2 seconds
}




function showBigSlides() {
  var i;
  var slides = document.getElementsByClassName("bimages fade");
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none";
  }
  slideBigIndex++;
  if (slideBigIndex > slides.length) {slideBigIndex = 1}
  slides[slideBigIndex-1].style.display = "block";
  setTimeout(showBigSlides, 2000); 
}







